export const catalog: Product[] = rawCatalog
  .split('\n')
  .slice(1)
  .map((line) => line.trim())
  .filter(Boolean)
  .map((line) => {
    const [source, category, name, size, unit, price, note] = line.split(';')

    return {
      source: source.trim(),
      category: category.trim(),
      name: name.trim(),
      size: (size ?? '').trim(),
      unit: (unit ?? '').trim(),
      price: price?.trim() ? Number.parseFloat(price) : null,
      note: (note ?? '').trim(),
    }
  })

export const categories = [...new Set(catalog.map((product) => product.category))].sort((a, b) =>
  a.localeCompare(b, 'uk'),
)

export function getCategoryStats(category: string) {
  const products = catalog.filter((product) => product.category === category)
  const prices = products
    .map((product) => product.price)
    .filter((price): price is number => price !== null)

  return {
    count: products.length,
    minPrice: prices.length ? Math.min(...prices) : null,
    maxPrice: prices.length ? Math.max(...prices) : null,
  }
}
